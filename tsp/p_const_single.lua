-- =============================================================================
-- p_const_single.lua
-- Скрипт поддержания постоянной мощности (P=const) на канале smua
-- Keithley 2604B TSP (Test Script Processor)
--
-- ВНИМАНИЕ: СКРИПТ УПРАВЛЯЕТ НАГРЕВАТЕЛЕМ ПРИ КРИОГЕННЫХ ТЕМПЕРАТУРАХ (~4K).
-- ЛЮБАЯ ОШИБКА В ЛОГИКЕ БЕЗОПАСНОСТИ МОЖЕТ ПРИВЕСТИ К УНИЧТОЖЕНИЮ ЭКСПЕРИМЕНТА.
-- ПЕРЕД ЛЮБЫМИ ИЗМЕНЕНИЯМИ — ТЩАТЕЛЬНЫЙ РЕВЬЮ.
-- =============================================================================

-- =============================================================================
-- ПАРАМЕТРЫ (устанавливаются хостом до запуска скрипта через TSP-переменные):
--   P_target       (float, Вт)  — целевая мощность
--   V_compliance   (float, В)   — максимально допустимое напряжение
--   I_compliance   (float, А)   — максимально допустимый ток
-- =============================================================================

-- -----------------------------------------------------------------------------
-- Глобальные переменные состояния
-- -----------------------------------------------------------------------------

-- Текущее установленное напряжение на источнике
V_current = 0.0

-- Флаг ошибки; читается хостом после завершения скрипта
-- "NONE" означает нормальное завершение
script_error = "NONE"

-- Последние измеренные значения (обновляются в каждой итерации цикла)
last_V = 0.0
last_I = 0.0
last_P = 0.0

-- =============================================================================
-- СТОРОЖЕВОЙ ТАЙМЕР (WATCHDOG) — КРИТИЧЕСКИ ВАЖНАЯ ФУНКЦИЯ БЕЗОПАСНОСТИ
--
-- Хост обязан вызывать heartbeat() не реже одного раза в 30 секунд.
-- Если связь с хостом прервана, скрипт автоматически отключит выход.
-- Таймаут намеренно консервативен: 30 с достаточно для любой штатной паузы
-- и при этом не допускает длительной работы без контроля хоста.
-- =============================================================================
watchdog_last_heartbeat = os.time()

-- heartbeat() — вызывается хостом периодически для подтверждения связи
function heartbeat()
    watchdog_last_heartbeat = os.time()
end

-- =============================================================================
-- get_status() — возвращает строку с текущим состоянием для опроса хостом
-- =============================================================================
function get_status()
    return string.format(
        "V=%.6f,I=%.9f,P=%.9f,error=%s",
        last_V, last_I, last_P, script_error
    )
end

-- =============================================================================
-- emergency_stop() — немедленное аварийное отключение выхода.
-- Может быть вызвана хостом в любой момент.
-- ВНИМАНИЕ: после вызова состояние скрипта считается недействительным;
-- для возобновления работы требуется повторный запуск скрипта.
-- =============================================================================
function emergency_stop()
    -- АВАРИЙНАЯ ОСТАНОВКА: немедленно снять напряжение и выключить выход
    smua.source.levelv = 0
    smua.source.output = smua.OUTPUT_OFF
    script_error = "EMERGENCY_STOP"
end

-- =============================================================================
-- safe_shutdown() — безопасное завершение: обнуляет напряжение и выключает
-- выход. Вызывается ВСЕГДА в конце работы скрипта (штатно или при ошибке).
-- =============================================================================
local function safe_shutdown()
    -- ВНИМАНИЕ: эта функция должна выполниться при любых обстоятельствах.
    -- Не добавлять сюда ничего, что может вызвать исключение.
    smua.source.levelv = 0
    smua.source.output = smua.OUTPUT_OFF
end

-- =============================================================================
-- Инициализация smua
-- =============================================================================
local function init_smua()
    -- Настройка режима источника напряжения
    smua.reset()
    smua.source.func = smua.OUTPUT_DCVOLTS

    -- Автоматический выбор диапазона источника
    smua.source.autorangev = smua.AUTORANGE_ON

    -- Автоматический выбор диапазона измерения тока
    smua.measure.autorangei = smua.AUTORANGE_ON

    -- Установка пределов соответствия (compliance) из параметров хоста
    -- ВНИМАНИЕ: превышение compliance может повредить образец или нагреватель
    smua.source.limitv = V_compliance
    smua.source.limiti = I_compliance

    -- Настройка буфера nvbuffer1
    smua.nvbuffer1.clear()
    smua.nvbuffer1.capacity = 10000
    smua.nvbuffer1.appendmode = 1
    -- Включить запись временных меток в буфер
    smua.nvbuffer1.collecttimestamps = 1

    -- Начальное напряжение — ноль (безопасный старт)
    smua.source.levelv = 0
    V_current = 0.0

    -- Включение выхода
    smua.source.output = smua.OUTPUT_ON
end

-- =============================================================================
-- Основной цикл обратной связи P=const
-- Целевая частота: 10 Гц (задержка 0.1 с на итерацию)
--
-- ВАЖНО: весь цикл обёрнут в pcall() — любое исключение Lua приводит к
-- safe_shutdown(), а не к зависанию скрипта с активным выходом.
-- =============================================================================
local function main_loop()

    -- Внутренний флаг ошибки (локальная копия до записи в глобал)
    local error_flag = nil

    local ok, err = pcall(function()

        while true do

            -- ------------------------------------------------------------------
            -- СТОРОЖЕВОЙ ТАЙМЕР — ПРОВЕРКА СВЯЗИ С ХОСТОМ
            -- ВНИМАНИЕ: если хост не прислал heartbeat более 30 секунд —
            -- немедленная аварийная остановка. Это главный защитный механизм.
            -- ------------------------------------------------------------------
            if (os.time() - watchdog_last_heartbeat) > 30 then
                -- Хост не отвечает: аварийное отключение
                smua.source.output = smua.OUTPUT_OFF
                error_flag = "WATCHDOG_TIMEOUT"
                break
            end

            -- ------------------------------------------------------------------
            -- Измерение V и I одновременно (без записи в буфер)
            -- ------------------------------------------------------------------
            local V, I = smua.measure.iv()

            -- ------------------------------------------------------------------
            -- ПРОВЕРКА COMPLIANCE — ЗАЩИТА ОТ ПЕРЕГРУЗКИ
            -- ВНИМАНИЕ: превышение допустимых пределов — немедленное отключение.
            -- Проверка выполняется ДО любых вычислений.
            -- ------------------------------------------------------------------
            if V > V_compliance or math.abs(I) > I_compliance then
                smua.source.output = smua.OUTPUT_OFF
                error_flag = "COMPLIANCE_VIOLATION"
                break
            end

            -- ------------------------------------------------------------------
            -- Обновление публичных переменных состояния
            -- ------------------------------------------------------------------
            last_V = V
            last_I = I
            last_P = V * I

            -- ------------------------------------------------------------------
            -- Регулятор P=const
            -- ------------------------------------------------------------------

            -- Защита от деления на ноль: пропустить итерацию если ток слишком мал
            if math.abs(I) < 1e-9 then
                -- Ток практически нулевой — расчёт R невозможен, ждём следующей итерации
                delay(0.1)
                goto continue
            end

            -- Расчёт сопротивления нагрузки
            local R = V / I

            -- ВНИМАНИЕ: R должно быть положительным; отрицательное R означает
            -- нештатную ситуацию (паразитный ток, КЗ и т.п.).
            -- Пропустить итерацию если R <= 0.
            if R <= 0 then
                delay(0.1)
                goto continue
            end

            -- Новое целевое напряжение из условия P = V^2 / R => V = sqrt(P * R)
            local V_new = math.sqrt(P_target * R)

            -- ------------------------------------------------------------------
            -- Ограничение скорости нарастания (slew rate) — защита от скачков
            -- напряжения. Максимальное изменение за одну итерацию: 0.5 В.
            -- ВНИМАНИЕ: не убирать и не увеличивать этот предел без анализа
            -- тепловой инерции системы.
            -- ------------------------------------------------------------------
            local delta = V_new - V_current
            local max_delta = 0.5  -- В/итерацию

            if math.abs(delta) > max_delta then
                -- Ограничить дельту с сохранением знака
                delta = max_delta * (delta >= 0 and 1 or -1)
                V_new = V_current + delta
            end

            -- Дополнительная защита: не превышать V_compliance
            if V_new > V_compliance then
                V_new = V_compliance
            end
            -- Напряжение не может быть отрицательным для нагревателя
            if V_new < 0 then
                V_new = 0
            end

            -- Применить новое напряжение
            smua.source.levelv = V_new
            V_current = V_new

            -- ------------------------------------------------------------------
            -- Запись точки в буфер (V и I измеряются повторно после установки
            -- нового напряжения — это фактическое состояние после шага регулятора)
            -- ------------------------------------------------------------------
            smua.measure.iv(smua.nvbuffer1)

            -- Метка для goto (пропуск итерации при нулевом токе)
            ::continue::

            -- Пауза для поддержания частоты цикла ~10 Гц
            delay(0.1)

        end -- while true

    end) -- pcall

    -- Если pcall перехватил исключение Lua — зафиксировать ошибку
    if not ok then
        -- ВНИМАНИЕ: сюда попадаем при любом необработанном исключении в цикле.
        -- Немедленно выключить выход.
        smua.source.output = smua.OUTPUT_OFF
        error_flag = "LUA_ERROR: " .. tostring(err)
    end

    -- Вернуть флаг ошибки (nil означает штатное завершение, если цикл прерван снаружи)
    return error_flag
end

-- =============================================================================
-- Точка входа: инициализация → главный цикл → безопасное завершение
-- =============================================================================

-- Инициализировать оборудование
init_smua()

-- Запустить главный цикл и сохранить результат
local result = main_loop()

-- =============================================================================
-- БЕЗОПАСНОЕ ЗАВЕРШЕНИЕ — выполняется ВСЕГДА, независимо от причины выхода
-- из цикла (штатное завершение, watchdog, compliance, ошибка Lua).
-- ВНИМАНИЕ: safe_shutdown() не должна вызывать исключений.
-- =============================================================================
safe_shutdown()

-- Записать финальный статус в глобальную переменную для чтения хостом
script_error = result or "NONE"
