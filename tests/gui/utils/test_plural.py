"""Phase III.D — Russian plural helper tests."""

from __future__ import annotations

import pytest

from cryodaq.gui.utils.plural import ru_plural


# ("критический", "критических", "критических") — severity noun
# ("предупреждение", "предупреждения", "предупреждений") — canonical
# ("канал", "канала", "каналов") — common
@pytest.mark.parametrize(
    ("n", "singular", "few", "many", "expected"),
    [
        # Singular forms — 1, 21, 31, 101.
        (1, "канал", "канала", "каналов", "канал"),
        (21, "канал", "канала", "каналов", "канал"),
        (31, "канал", "канала", "каналов", "канал"),
        (101, "канал", "канала", "каналов", "канал"),
        # Few forms — 2-4, 22-24, 32-34.
        (2, "канал", "канала", "каналов", "канала"),
        (3, "канал", "канала", "каналов", "канала"),
        (4, "канал", "канала", "каналов", "канала"),
        (22, "канал", "канала", "каналов", "канала"),
        (33, "канал", "канала", "каналов", "канала"),
        # Many forms — 0, 5-20, 25, 100.
        (0, "канал", "канала", "каналов", "каналов"),
        (5, "канал", "канала", "каналов", "каналов"),
        (10, "канал", "канала", "каналов", "каналов"),
        (11, "канал", "канала", "каналов", "каналов"),
        (14, "канал", "канала", "каналов", "каналов"),
        (15, "канал", "канала", "каналов", "каналов"),
        (20, "канал", "канала", "каналов", "каналов"),
        (25, "канал", "канала", "каналов", "каналов"),
        (100, "канал", "канала", "каналов", "каналов"),
        # Severity nouns — the original bug surface.
        (
            1,
            "критический",
            "критических",
            "критических",
            "критический",
        ),
        (
            1,
            "предупреждение",
            "предупреждения",
            "предупреждений",
            "предупреждение",
        ),
        (
            2,
            "предупреждение",
            "предупреждения",
            "предупреждений",
            "предупреждения",
        ),
        (
            5,
            "предупреждение",
            "предупреждения",
            "предупреждений",
            "предупреждений",
        ),
    ],
)
def test_ru_plural(n: int, singular: str, few: str, many: str, expected: str) -> None:
    assert ru_plural(n, singular, few, many) == expected


def test_ru_plural_handles_negative_numbers() -> None:
    assert ru_plural(-1, "a", "b", "c") == "a"
    assert ru_plural(-3, "a", "b", "c") == "b"
    assert ru_plural(-15, "a", "b", "c") == "c"
