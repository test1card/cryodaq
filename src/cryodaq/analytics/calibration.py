"""Хранилище калибровочных кривых (заглушка).

В настоящее время калибровочные кривые DT-670B1-CU хранятся
непосредственно в мониторах LakeShore 218S.  Данный модуль
предназначен для будущей реализации независимой перекалибровки
датчиков согласно ГОСТ Р 8.879-2014 «Государственная поверочная
схема для средств измерений температуры».

Планируемая функциональность:
    - Загрузка индивидуальных калибровочных кривых из файлов
      (формат LakeShore .340 или CSV).
    - Преобразование напряжение → температура для кремниевых диодов.
    - Преобразование сопротивление → температура для Cernox/Pt.
    - Интерполяция кубическими сплайнами по табличным данным.
    - Учёт магнитного поля (поправочные коэффициенты).
    - Валидация кривых: монотонность, диапазон, отклонение от эталона.

Тип датчиков:
    DT-670B1-CU — кремниевые диоды, диапазон 1.4–500 К,
    индивидуальные кривые на каждый датчик.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any

_NOT_IMPLEMENTED_MSG = (
    "Калибровка ещё не реализована — используются встроенные кривые "
    "мониторов LakeShore 218S.  См. ГОСТ Р 8.879-2014."
)


class CalibrationStore:
    """Хранилище калибровочных кривых для температурных датчиков.

    .. warning::
        Все методы выбрасывают ``NotImplementedError``.
        Модуль является заглушкой для будущей реализации.

    Планируемый API::

        store = CalibrationStore()
        store.load_curves(Path("config/calibration/"))
        T = store.voltage_to_temp("DT-670-S/N-12345", voltage=0.5623)
        T = store.resistance_to_temp("Cernox-CX-1050-AA", resistance=1823.5)
    """

    def load_curves(self, curves_dir: Path) -> None:
        """Загрузить калибровочные кривые из директории.

        Параметры
        ----------
        curves_dir:
            Директория с файлами кривых (.340, .csv).

        Исключения
        ----------
        NotImplementedError:
            Всегда — модуль ещё не реализован.
        """
        raise NotImplementedError(_NOT_IMPLEMENTED_MSG)

    def voltage_to_temp(
        self, sensor_id: str, voltage: float, *, magnetic_field_T: float = 0.0,
    ) -> float:
        """Преобразовать напряжение кремниевого диода в температуру.

        Параметры
        ----------
        sensor_id:
            Серийный номер датчика (например, ``"DT-670-S/N-12345"``).
        voltage:
            Измеренное напряжение (В).
        magnetic_field_T:
            Магнитное поле в точке датчика (Тл).  По умолчанию 0.

        Возвращает
        ----------
        float:
            Температура в Кельвинах.

        Исключения
        ----------
        NotImplementedError:
            Всегда — модуль ещё не реализован.
        """
        raise NotImplementedError(_NOT_IMPLEMENTED_MSG)

    def resistance_to_temp(
        self, sensor_id: str, resistance: float, *, magnetic_field_T: float = 0.0,
    ) -> float:
        """Преобразовать сопротивление терморезистора в температуру.

        Параметры
        ----------
        sensor_id:
            Серийный номер датчика (например, ``"Cernox-CX-1050-AA"``).
        resistance:
            Измеренное сопротивление (Ом).
        magnetic_field_T:
            Магнитное поле в точке датчика (Тл).  По умолчанию 0.

        Возвращает
        ----------
        float:
            Температура в Кельвинах.

        Исключения
        ----------
        NotImplementedError:
            Всегда — модуль ещё не реализован.
        """
        raise NotImplementedError(_NOT_IMPLEMENTED_MSG)

    def get_curve_info(self, sensor_id: str) -> dict[str, Any]:
        """Получить метаданные калибровочной кривой.

        Параметры
        ----------
        sensor_id:
            Серийный номер датчика.

        Возвращает
        ----------
        dict:
            Метаданные: тип датчика, диапазон, дата калибровки, точность.

        Исключения
        ----------
        NotImplementedError:
            Всегда — модуль ещё не реализован.
        """
        raise NotImplementedError(_NOT_IMPLEMENTED_MSG)
